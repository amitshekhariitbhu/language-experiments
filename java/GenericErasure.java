import java.util.ArrayList;
import java.util.List;

/**
 * GenericErasure.java
 *
 * Java generics are implemented via "type erasure": the compiler checks
 * types at compile time, then erases all generic type information in the
 * bytecode. At runtime, a List<String> and List<Integer> are both just List.
 *
 * This has many surprising consequences: you can't do instanceof with
 * generics, can't create generic arrays, and can defeat type safety
 * with raw types.
 */
public class GenericErasure {
    public static void main(String[] args) {
        System.out.println("=== Generic Type Erasure Experiments ===\n");

        // Experiment 1: At runtime, all Lists are the same class
        List<String> strings = new ArrayList<>();
        List<Integer> ints = new ArrayList<>();
        System.out.println("1. Runtime class of List<String> vs List<Integer>:");
        System.out.println("   strings.getClass(): " + strings.getClass());
        System.out.println("   ints.getClass()   : " + ints.getClass());
        System.out.println("   Same class?       : " + (strings.getClass() == ints.getClass())); // true!

        // Experiment 2: You can defeat generics with raw types (unchecked)
        @SuppressWarnings("unchecked")
        List<String> sneaky = (List<String>)(List<?>) ints;
        // We just cast a List<Integer> to List<String> -- no runtime error!
        // The cast is "unchecked" because there's nothing to check at runtime.
        System.out.println("\n2. Casting List<Integer> to List<String>:");
        System.out.println("   Cast succeeded with no exception (type erasure)");

        // Experiment 3: Adding wrong type via raw type
        @SuppressWarnings({"unchecked", "rawtypes"})
        List rawList = strings;   // raw type reference
        rawList.add(42);          // adds an Integer to a "List<String>" -- compiles and runs!
        rawList.add("hello");
        System.out.println("\n3. Added Integer 42 to a List<String> via raw type:");
        System.out.println("   List contents: " + rawList);
        // The error only surfaces when you try to USE the element as a String:
        try {
            String s = strings.get(0);  // ClassCastException here
            System.out.println("   Got string: " + s);
        } catch (ClassCastException e) {
            System.out.println("   ClassCastException when reading element 0 as String!");
            System.out.println("   Message: " + e.getMessage());
        }

        // Experiment 4: You cannot create a generic array
        // This would not compile: new T[10] or new List<String>[10]
        // Workaround with Object array and cast:
        @SuppressWarnings("unchecked")
        List<String>[] arrayOfLists = (List<String>[]) new List<?>[3];
        arrayOfLists[0] = new ArrayList<>();
        arrayOfLists[0].add("works");
        System.out.println("\n4. Generic array workaround:");
        System.out.println("   arrayOfLists[0] = " + arrayOfLists[0]);

        // Experiment 5: instanceof cannot use parameterized types
        // This would not compile: if (strings instanceof List<String>)
        // You can only check against the raw type:
        System.out.println("\n5. instanceof with generics:");
        System.out.println("   strings instanceof List : " + (strings instanceof List));
        // Cannot write: strings instanceof List<String>  -- compile error

        // Experiment 6: Method overloading with different generic types
        // You cannot have both: void foo(List<String>) and void foo(List<Integer>)
        // because after erasure they'd both be foo(List) -- name clash.
        System.out.println("\n6. Overloading with generics:");
        System.out.println("   Cannot overload foo(List<String>) and foo(List<Integer>)");
        System.out.println("   After erasure both become foo(List) -- compile error");

        // Experiment 7: Bridge methods
        System.out.println("\n7. Bridge methods generated by compiler:");
        System.out.println("   Comparable<String> has compareTo(String), but after erasure");
        System.out.println("   the interface has compareTo(Object). The compiler generates");
        System.out.println("   a synthetic 'bridge' method to maintain polymorphism.");
        // Demonstrate by checking method count:
        java.lang.reflect.Method[] methods = MyComparable.class.getDeclaredMethods();
        for (java.lang.reflect.Method m : methods) {
            System.out.println("   Method: " + m.getName() + "(" +
                java.util.Arrays.stream(m.getParameterTypes())
                    .map(Class::getSimpleName)
                    .reduce((x,y) -> x+", "+y).orElse("") +
                ") isBridge=" + m.isBridge());
        }
    }

    // Helper class to demonstrate bridge methods
    static class MyComparable implements Comparable<MyComparable> {
        @Override
        public int compareTo(MyComparable other) {
            return 0;
        }
    }
}
